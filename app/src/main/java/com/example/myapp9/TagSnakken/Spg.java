package com.example.myapp9.TagSnakken;

public class Spg {

    private String mName;
    private int mIconId;

    Spg(String name, int iconId) {
        mName = name;
        mIconId = iconId;
    }

    public String getName() {
        return mName;
    }

    public int getIconId() {
        return mIconId;
    }
}
